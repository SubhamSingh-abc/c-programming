/*
1. Zoom the Entire VS Code (Editor + Terminal)
Zoom in: Ctrl + = (or Ctrl + Shift + =)
Zoom out: Ctrl + -
Reset zoom: Ctrl + 0
This changes everything, including editor and terminal.
2. Zoom Only the Terminal
If you want terminal-only zoom, you can do it with keybindings:
Open Keyboard Shortcuts: Ctrl + K Ctrl + S
Search for: Increase Terminal Font Size and Decrease Terminal Font Size.
Assign your own shortcuts, e.g.:
Increase → Ctrl + Alt + +
Decrease → Ctrl + Alt + -
Now you can instantly resize the terminal font without affecting the editor.
*/